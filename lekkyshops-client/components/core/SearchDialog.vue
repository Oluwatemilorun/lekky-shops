<template>
  <v-dialog v-model="dialog" fullscreen>
    <v-card flat tile>
      <v-card-title>
        <v-spacer />
        <v-btn icon @click="dialog = false">
          <v-icon>mdi-close</v-icon>
        </v-btn>
      </v-card-title>
      <v-card-text>
        <v-container>
          <v-row justify="center" align="center">
            <v-col cols="12" class="mb-2">
              <v-text-field
                v-model="search"
                placeholder="Search products and vendors"
                prepend-inner-icon="mdi-magnify"
              />
            </v-col>
          </v-row>
        </v-container>
      </v-card-text>
    </v-card>
  </v-dialog>
</template>

<script>
export default {
  props: {
    value: {
      type: Boolean,
      default: false,
    },
  },
  data() {
    return {
      search: '',
    }
  },
  computed: {
    dialog: {
      get() {
        return this.value
      },
      set(value) {
        this.$emit('input', value)
      },
    },
  },
}
</script>

